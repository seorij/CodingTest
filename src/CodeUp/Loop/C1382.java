package CodeUp.Loop;

public class C1382 {
    public static void main(String[] args) {
            for(int j = 1; j <= 9; j++) {
                for(int i = 2; i <= 5; i++) {
                    System.out.printf("%d x %d = %2d\t",i, j, i*j);
            }
            System.out.println();
        }
    }
}
